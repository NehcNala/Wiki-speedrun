package alan;

public class Input {


    private String start;


    private String finish;


    private int depth;


    public String getStart(){
        return start;
    }


    public void setStart(String start){
        this.start = start;
    }


    public String getFinish(){
        return finish;
    }


    public void setFinish(String finish){
        this.finish = finish;
    }


    public int getDepth(){
        return depth;
    }


    public void setDepth(int depth){
        this.depth = depth;
    }
}
